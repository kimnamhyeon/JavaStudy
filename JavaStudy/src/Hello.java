
public class Hello {
	void btn1() {
		System.out.println("1번 버튼 누름");
	}
	void btn100() {
		System.out.println("100번 버튼 누름");
	}
	
	void btn(int btn) {
		System.out.println(btn + "번 버튼 누름");
	}
	
	
	// main 입력 후 => Ctrl + Space
	public static void main(String[] args) {
		System.out.println((int)Math.pow(2, 38));
		System.out.println((long)Math.pow(2, 38));
		
		// syso 입력 후 => Ctrl + Space
		System.out.println("Hello~~~");

		// 코드 정렬 => Ctrl + Shift + F
		int 인원 = 10;
		int 점수 = 100;

		System.out.println(인원 * 점수);
	}
}
